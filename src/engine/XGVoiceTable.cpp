bool isXGVoice(uint8_t msb, uint8_t lsb, uint8_t program) {
    uint32_t key = (static_cast<uint32_t>(msb) << 16) | 
                   (static_cast<uint32_t>(lsb) << 8) | 
                   static_cast<uint32_t>(program);
    const auto& xgMap = getXGVoiceMap();
    return xgMap.find(key) != xgMap.end();
}

std::string getXGVoiceName(uint8_t msb, uint8_t lsb, uint8_t program) {
    uint32_t key = (static_cast<uint32_t>(msb) << 16) | 
                   (static_cast<uint32_t>(lsb) << 8) | 
                   static_cast<uint32_t>(program);
    const auto& xgMap = getXGVoiceMap();
    auto it = xgMap.find(key);
    if (it != xgMap.end()) {
        return it->second;
    }
    return "Unknown Voice";
}

